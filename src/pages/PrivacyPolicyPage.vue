<script setup>
import { ref, onMounted, onUnmounted } from 'vue';

// Table of contents data
const sections = ref([
  { id: 1, title: 'Información que Recopilamos' },
  { id: 2, title: 'Cómo Usamos Su Información' },
  { id: 3, title: 'Cómo Compartimos Su Información' },
  { id: 4, title: 'Seguridad de los Datos' },
  { id: 5, title: 'Sus Derechos de Privacidad' },
  { id: 6, title: 'Retención de Datos' },
  { id: 7, title: 'Privacidad de los Niños' },
  { id: 8, title: 'Cambios en Esta Política de Privacidad' },
  { id: 9, title: 'Contacto' },
]);

const activeSection = ref(1);

// Scroll spy for active section
const observer = ref(null);
onMounted(() => {
  observer.value = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          const id = parseInt(entry.target.id.replace('section-', ''));
          activeSection.value = id;
        }
      });
    },
    { threshold: 0.5 }
  );

  sections.value.forEach((section) => {
    const element = document.getElementById(`section-${section.id}`);
    if (element) observer.value.observe(element);
  });
});

onUnmounted(() => {
  if (observer.value) {
    observer.value.disconnect();
  }
});
</script>

